#!/usr/bin/env bash
# Приклад базових команд Docker для ознайомлення
# Виконуйте по черзі в терміналі (Docker має бути встановлений)

# Перевірка версії Docker
docker --version

# Завантажити офіційний тестовий образ і запустити контейнер
# Після виконання контейнер виведе повідомлення і завершиться
docker run hello-world

# Список запущених контейнерів (після hello-world контейнер вже зупинений)
docker ps

# Список усіх контейнерів (включно з зупиненими)
docker ps -a

# Список локальних образів (з’явиться hello-world)
docker images

# Видалити контейнер за іменем або ID (опційно, для очищення)
# docker rm <container_id>
# Видалити образ (опційно)
# docker rmi hello-world
